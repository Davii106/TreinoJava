import java.util.Scanner;

public class Tabuada {
    public static void main(String[] args) {
        Scanner ler = new Scanner(System.in);

        System.out.print("Digite um número: ");
        int numero = ler.nextInt();
        int tabuada2 = numero *2;
        System.out.println("O segundo número de sua tabuada é: "+tabuada2);
        int tabuada3 = numero *3;
        System.out.println("O terceiro número de sua tabuada é: "+tabuada3);
        int tabuada4 = numero *4;
        System.out.println("O quarto número de sua tabuada é: "+tabuada4);
        int tabuada5 = numero *5;
        System.out.println("O quinto número de sua tabuada é: "+tabuada5);
        int tabuada6 = numero *6;
        System.out.println("O sexto número de sua tabuada é: "+tabuada6);
        int tabuada7 = numero *7;
        System.out.println("O sétimo número de sua tabuada é: "+tabuada7);
        int tabuada8 = numero *8;
        System.out.println("O oitavo número de sua tabuada é: "+tabuada8);
        int tabuada9 = numero *9;
        System.out.println("O nono número de sua tabuada é: "+tabuada9);
        int tabuada10 = numero *10;
        System.out.println("O décimo número de sua tabuada é: "+tabuada10);
    }
}
